#comparison_operator
statement1=input("Enter first statement")
statement2=input("Enter second statement")

print(len(statement1))
print(len(statement2))
#==
print(len(statement1)==len(statement2))
#!=
print(len(statement1)!=len(statement2))
#<
print(len(statement1)<len(statement2))
#>
print(len(statement1)>len(statement2))
#<=
print(len(statement1)<=len(statement2))
#>=
print(len(statement1)>=len(statement2))
