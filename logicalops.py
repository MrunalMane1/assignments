#logical_operators
statement1="I am Mrunal"
statement2="I am a good programmer"

#using not
print(not statement1)
print(not statement2)
#using and
print(statement1 and statement2)
#using or
print(statement1 or statement2)
#using not & and
print(not statement1 and statement2)
#using not & or
print(not statement1 or statement2)

